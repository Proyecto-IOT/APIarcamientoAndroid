package com.example.apiarcamento.models;

class Credentials {
    private User user;
    private String token;

    public User getUser() {
        return user;
    }

    public void setUser(User value) {
        this.user = value;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String value) {
        this.token = value;
    }
}
